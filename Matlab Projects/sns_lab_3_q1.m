t=0:0.1:5;
t1=0:1/3:5;
x1=cos(2*pi*t);
x2=cos(14*pi*t);
subplot(3,3,1), plot(t,x1);
title('sinusoidal x1');
grid on;
subplot(3,3,2), stem(t,x2);
title('sinusoidal x2');
grid on;
n=3;
d_x1=cos(2*pi*t1);
d_x2=cos(14*pi*t1);
subplot(3,3,3), stem(t1,d_x1);
title('discrete x1');
grid on;
subplot(3,3,4), plot(t1,d_x2);
title('discrete x2');
grid on;
subplot(3,3,5), plot(spline(t1, d_x1, t));
title('Reconstructed x1');
grid on;
subplot(3,3,6), plot(spline(t1, d_x2, t));
title('Reconstructed x2');
grid on;
